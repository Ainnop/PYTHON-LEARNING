def flows():
    x = 100
    y = 20
    if x < y:
        print("x < y is true")
    else:
        print("x < y is false")


def nested_ifs():
    x = 20
    y = 20
    if x < y:
        print("x < y is true")
    elif x > y:
        print("x > y is true")
    elif x == y:
        print("x = y is true")
    else:
        print("there is no condition for x and y")
